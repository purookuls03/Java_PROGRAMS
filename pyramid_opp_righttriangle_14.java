public class pyramid_opp_righttriangle_14 {
    public static void main(String[] args) {
        int num = 6;
        int i,j;
        for(i=1;i<=num;i++)
        {
            for(j=num;j>i;j--)
            {
                System.out.print("* ");
            }
            System.out.println();
        }
          
    }
    
}
